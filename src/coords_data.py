# 재개발사업 리스트는 VWorld 지오코딩 결과 CSV에서 동적 로드.
# (구 하드코딩 6건은 제거됨 — geocode_redev.py로 활성 120건 처리)


def load_redev_projects(only_active=True):
    """data/processed/redev_projects_geocoded.csv 읽어 dict 리스트 반환.

    Args:
        only_active: True면 상태='진행' 사업만, False면 전체.

    Returns:
        [{사업명, 구분, 구, 동, 주소, 면적_m2, 세대수, 추진현황, 추진일자,
          단계_순서, 통학영향_임박도, 촉진지구, 추진지구, 좌표(lat,lon), 상태},
         ...] — 좌표 없는 사업은 제외.
    """
    import pandas as pd
    from pathlib import Path

    csv_path = Path(__file__).parent.parent / "data" / "processed" / "redev_projects_geocoded.csv"
    if not csv_path.exists():
        raise FileNotFoundError(
            f"{csv_path} 없음. python -m src.geocode_redev 먼저 실행"
        )

    df = pd.read_csv(csv_path)
    df = df[df["lat"].notna() & df["lon"].notna()].copy()
    if only_active:
        df = df[df["상태"] == "진행"]

    # 동: 주소에서 추출 ("동구 대동 405-7" → "대동")
    def extract_dong(addr):
        import re
        m = re.search(r"(?:대덕구|동구|중구|서구|유성구)\s+([가-힣]+동)", str(addr))
        return m.group(1) if m else None

    out = []
    for _, r in df.iterrows():
        out.append({
            "사업명": r["구역명"],
            "구분": r.get("구분", ""),
            "구": r["구"],
            "동": extract_dong(r.get("전체주소", "")),
            "주소": r.get("전체주소", ""),
            "면적_m2": r.get("면적_m2", None),
            "세대수": r.get("세대수", None),
            "추진현황": r.get("추진현황", ""),
            "추진일자": r.get("추진일자", ""),
            "단계_순서": r.get("단계_순서", None),
            "통학영향_임박도": r.get("통학영향_임박도", ""),
            "촉진지구": r.get("촉진지구", False),
            "추진지구": r.get("추진지구", ""),
            "상태": r.get("상태", ""),
            "좌표": (float(r["lat"]), float(r["lon"])),
        })
    return out
